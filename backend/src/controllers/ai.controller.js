// ── POST /api/ai/suggest-assignee ─────────────────────────────────────────────
export const suggestAssignee = async (req, res, next) => {
  try {
    const { taskTitle, taskDescription, projectId } = req.body;

    const project = await Project.findById(projectId).populate('members.user', 'name');
    if (!project) return res.status(404).json({ success: false, message: 'Project not found' });

    // Get workload per member
    const memberIds = project.members.map(m => m.user._id);
    const workloads = await Promise.all(
      memberIds.map(async (id) => {
        const count = await Task.countDocuments({ assignees: id, status: { $ne: 'done' }, project: projectId });
        const member = project.members.find(m => m.user._id.toString() === id.toString());
        return { id, name: member?.user?.name, activeTasks: count };
      })
    );

    const raw = await askAI(
      `Suggest the best team member to assign this task based on workload and task type.

Task: "${taskTitle}"
Description: "${taskDescription || 'None'}"

Team workloads (active tasks):
${workloads.map(w => `- ${w.name}: ${w.activeTasks} active tasks`).join('\n')}

Respond ONLY with JSON: {
  "suggestedName": "name of recommended member",
  "reason": "1-sentence explanation",
  "workloadWarning": "any overload concern or null"
}`
    );

    const data = parseJSON(raw);
    if (!data) return res.status(502).json({ success: false, message: 'AI response parsing failed' });

    const suggested = workloads.find(w => w.name?.toLowerCase().includes(data.suggestedName?.toLowerCase()));
    res.json({ success: true, suggestion: { ...data, memberId: suggested?.id, workloads } });
  } catch (err) { next(err); }
};

// ── GET /api/ai/health/:projectId ─────────────────────────────────────────────
export const projectHealthScore = async (req, res, next) => {
  try {
    const project = await Project.findById(req.params.projectId);
    if (!project) return res.status(404).json({ success: false, message: 'Project not found' });
    if (!project.isMember(req.user._id)) {
      return res.status(403).json({ success: false, message: 'Access denied' });
    }

    const tasks = await Task.find({ project: project._id })
      .select('title status priority dueDate estimatedHours actualHours createdAt');

    const now = new Date();
    const overdueTasks = tasks.filter(t => t.dueDate && t.dueDate < now && t.status !== 'done');
    const totalTasks = tasks.length;
    const doneTasks = tasks.filter(t => t.status === 'done').length;
    const criticalOpen = tasks.filter(t => t.priority === 'critical' && t.status !== 'done').length;
    const daysUntilDue = project.dueDate ? Math.ceil((project.dueDate - now) / (1000 * 60 * 60 * 24)) : null;

    const raw = await askAI(
      `Analyze this project and return a health score.

Project: "${project.name}"
Status: ${project.status}
Due date: ${project.dueDate ? project.dueDate.toISOString().split('T')[0] : 'Not set'}
Days until due: ${daysUntilDue ?? 'N/A'}
Total tasks: ${totalTasks}
Completed tasks: ${doneTasks}
Overdue tasks: ${overdueTasks.length}
Critical open tasks: ${criticalOpen}
Completion rate: ${totalTasks ? Math.round((doneTasks / totalTasks) * 100) : 0}%

Overdue task titles: ${overdueTasks.slice(0, 5).map(t => t.title).join(', ') || 'None'}

Respond ONLY with JSON: {
  "score": 0-100,
  "status": "green|yellow|red",
  "summary": "2-3 sentence project health summary",
  "risks": ["risk 1", "risk 2", "risk 3"],
  "recommendations": ["action 1", "action 2"]
}`
    );

    const data = parseJSON(raw);
    if (!data) return res.status(502).json({ success: false, message: 'AI response parsing failed' });

    // Persist health score to project
    await Project.findByIdAndUpdate(project._id, {
      healthScore: {
        score: data.score,
        status: data.status,
        summary: data.summary,
        risks: data.risks,
        lastAnalyzedAt: new Date(),
      },
    });

    res.json({ success: true, health: data });
  } catch (err) { next(err); }
};

// ── POST /api/ai/chat ──────────────────────────────────────────────────────────
export const aiChat = async (req, res, next) => {
  try {
    if (!openai) {
      return res.status(503).json({ success: false, message: 'OpenAI API key is not configured' });
    }

    const { messages } = req.body;
    if (!messages?.length) {
      return res.status(400).json({ success: false, message: 'messages required' });
    }

    const response = await openai.chat.completions.create({
      model: 'gpt-4o',
      messages: [
        { role: 'system', content: PROJECTFLOW_SYSTEM_PROMPT },
        ...messages.map((m) => ({ role: m.role, content: m.content })),
      ],
      max_tokens: 1500,
    });

    res.json({ success: true, message: response.choices[0].message.content });
  } catch (err) { next(err); }
};
