// ── POST /api/ai/estimate ──────────────────────────────────────────────────────
export const estimateTask = async (req, res, next) => {
  try {
    const { title, description, priority = 'medium' } = req.body;
    if (!title) return res.status(400).json({ success: false, message: 'title required' });

    const raw = await askAI(
      `Estimate the time to complete this software task.
Title: "${title}"
Description: "${description || 'None provided'}"
Priority: ${priority}

Respond ONLY with JSON: { "estimatedHours": number, "complexityScore": 1-10, "reasoning": "2-sentence explanation", "breakdown": ["subtask with estimate", ...] }`
    );

    const data = parseJSON(raw);
    if (!data) return res.status(502).json({ success: false, message: 'AI response parsing failed' });

    res.json({ success: true, estimate: data });
  } catch (err) { next(err); }
};

