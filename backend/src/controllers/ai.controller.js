const OPENAI_API_KEY = process.env.OPENAI_API_KEY;
let openai = null;
if (OPENAI_API_KEY) {
  openai = new OpenAI({ apiKey: OPENAI_API_KEY });
} else {
  console.warn('⚠️  OPENAI_API_KEY is not set. AI endpoints will be unavailable.');
}

const askAI = async (prompt, system = '') => {
  if (!openai) {
    const err = new Error('OpenAI API key is not configured. Set OPENAI_API_KEY in .env');
    err.status = 503;
    throw err;
  }

  const completion = await openai.chat.completions.create({
    model: 'gpt-4o-mini',
    max_tokens: 1024,
    messages: [
      { role: 'system', content: system || 'You are a project management AI assistant. Always respond with valid JSON only — no markdown, no explanation.' },
      { role: 'user', content: prompt }
    ],
  });
  return completion.choices[0].message.content;
};

const parseJSON = (raw) => {
  try {
    const cleaned = raw.replace(/```json\n?|\n?```/g, '').trim();
    return JSON.parse(cleaned);
  } catch {
    return null;
  }
};

